export type AtlasCatalogCategory = 'dashboards' | 'tables' | 'charts' | 'kpis';

export interface AtlasCatalogItem {
  id: string;
  label: string;
  description: string;
  category: AtlasCatalogCategory;
  subcategory: string;
  icon: string;
  /** The source ID used to fetch data via blockDataService */
  source: string;
  /** Default block type when dropped into the report */
  blockType: 'table' | 'chart' | 'kpi' | 'kpi-grid' | 'dashboard';
  /** Whether this needs a period filter */
  periodBound: boolean;
  /** Tags for search */
  tags: string[];
  /** Is this a new addition to the catalog? */
  isNew?: boolean;
}

